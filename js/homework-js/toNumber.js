// TO NUMBER

// String to Number
let value = 'Julia';

// 1)
let toNumber = Number(value);
console.log(`${value}, значение через конструктор Number()`, toNumber);
console.log(`тип данных ${value}, значение через конструктор Number()`, typeof toNumber);

// 2)
toNumber = +value;
console.log(`${value}, значение через унарный плюс`, toNumber);
console.log(`тип данных ${value}, значение через унарный плюс`, typeof toNumber);

